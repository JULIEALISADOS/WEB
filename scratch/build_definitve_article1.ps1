<h3 id="porosidad-capilar">A. La porosidad y el historial químico de la fibra</h3>
<p>La porosidad determina la capacidad del cabello para absorber y retener agua y nutrientes. Un cabello con porosidad alta (común en melenas decoloradas, con balayage o sometidas a tintes frecuentes) posee una cutícula abierta. Aunque absorbe los aminoácidos del alisado con rapidez, los pierde de igual forma si no se aplica una mascarilla de recarga lipídica semanal.</p>

<h3 id="patron-rizo-crecimiento">B. El patrón de rizo original y el ritmo de crecimiento folicular</h3>
<p>En cabellos ondulados o de textura fina, el alisado orgánico muestra una fijación prolongada (alcanzando fácilmente los 8 meses). En cabellos afrocrespos o con rizo muy apretado, los medios y puntas tratados permanecen lisos, pero el crecimiento biológico de la raíz (1 a 1.5 cm por mes) hace necesario un retoque hacia el cuarto mes.</p>

<h3 id="grosor-densidad">C. El grosor del tallo capilar</h3>
<p>Las hebras gruesas y de alta densidad requieren un mayor tiempo de exposición en cabina y una técnica de sellado térmico extremadamente minuciosa. Cuando el sellado en salón es ejecutado por profesionales expertas, la retención proteica alcanza su máximo potencial.</p>

